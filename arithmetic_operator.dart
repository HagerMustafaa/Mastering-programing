void main() {
  int num1 = 2;
  int num2 = 6;
  int result = num1 + num2;
  print(result);

  // we cannot use / => maybe it's double not int/ we can use ~/

  int num3 = 15;
  int num4 = 10;
  int result2 = num3 ~/ num4;
  print(result2);

  double result3 = num3 / num4;
  print(result3);

  // % it gives int value

  int result4 = num3 % num4;
  print(result4);
}
